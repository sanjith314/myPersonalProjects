public class Range {
    public static void main(String[] args) {
        System.out.println("Range of int= "+Integer.BYTES);
        System.out.println("Range of long= "+Long.BYTES);
        System.out.println("Range of short= "+Short.BYTES);
        System.out.println("Range of double= "+Double.BYTES);
        System.out.println("Range of float= "+Float.BYTES);
        System.out.println("Range of byte= "+Byte.BYTES);

    }
}
